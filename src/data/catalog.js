export function getSection(sectionId) {
  return CATALOG[sectionId];
}

export function getSectionMeta(sectionId) {
  return MAIN_SECTIONS.find((section) => section.id === sectionId);
}

export function getItem(sectionId, itemId) {
  return CATALOG[sectionId]?.items?.find((item) => item.id === itemId);
}

export function getRouteLevel(levelId) {
  return CATALOG.routes.levels.find((level) => level.id === levelId);
}

export function getRoutesByLevel(levelId) {
  return CATALOG.routes.items.filter((route) => route.level === levelId);
}

export function getRoute(routeId) {
  return CATALOG.routes.items.find((route) => route.id === routeId);
}

function maps(query) {
  return `https://yandex.ru/maps/?mode=search&text=${encodeURIComponent(query)}`;
}

function photos(query) {
  return [
    { label: "Фото 1", url: photoSearch(`${query} панорама`) },
    { label: "Фото 2", url: photoSearch(`${query} прогулка`) },
    { label: "Фото 3", url: photoSearch(`${query} виды`) },
    { label: "Фото 4", url: photoSearch(`${query} фото`) }
  ];
}

function photoSearch(query) {
  return `https://yandex.ru/images/search?text=${encodeURIComponent(query)}`;
}
